def decodeMorse(morse_code):
    result = []
    morse_code = morse_code.strip()
    for word in morse_code.split(' ' * 3):
        acc = []
        for letter in word.split(' '):
            acc.append(MORSE_CODE[letter])
        result.append(''.join(acc))
    return ' '.join(result)
